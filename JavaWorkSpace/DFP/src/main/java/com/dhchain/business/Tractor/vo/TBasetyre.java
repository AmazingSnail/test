package com.dhchain.business.Tractor.vo;

import javax.persistence.Column;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

public class TBasetyre {
    /**
     * 主键
     */
    @Id
    @Column(name = "id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;
    private  String zmach;
    private String tyresize;
    private String wheelsize;
    private String tirespect;
    private  String inputman;
    private String inputtime;
    private String modifyman;
    private  String modifytime;
    private String plant;

    public String getPlant() {
        return plant;
    }

    public void setPlant(String plant) {
        this.plant = plant;
    }

    public String getInputman() {
        return inputman;
    }

    public void setInputman(String inputman) {
        this.inputman = inputman;
    }

    public String getInputtime() {
        return inputtime;
    }

    public void setInputtime(String inputtime) {
        this.inputtime = inputtime;
    }

    public String getModifyman() {
        return modifyman;
    }

    public void setModifyman(String modifyman) {
        this.modifyman = modifyman;
    }

    public String getModifytime() {
        return modifytime;
    }

    public void setModifytime(String modifytime) {
        this.modifytime = modifytime;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getZmach() {
        return zmach;
    }

    public void setZmach(String zmach) {
        this.zmach = zmach;
    }

    public String getTyresize() {
        return tyresize;
    }

    public void setTyresize(String tyresize) {
        this.tyresize = tyresize;
    }

    public String getWheelsize() {
        return wheelsize;
    }

    public void setWheelsize(String wheelsize) {
        this.wheelsize = wheelsize;
    }

    public String getTirespect() {
        return tirespect;
    }

    public void setTirespect(String tirespect) {
        this.tirespect = tirespect;
    }
}
